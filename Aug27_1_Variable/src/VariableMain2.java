
public class VariableMain2 {
	public static void main(String[] args) {
		// 레스토랑 이름이 김밥천국 저장
		// 출력
		// 레스토랑 크기가 100.386평 저장
		// 출력
		// 레스토랑 테이블이 20개 저장
		// 출력
		
		
		String name;
		name = "김밥천국";
		System.out.println(name);
		
		double size;
		size = 100.386;
		System.out.println(size);
		
		byte how_many_tables;
		how_many_tables = 20;
		System.out.println(how_many_tables);		
		
		boolean open;
		open = true;
		System.out.println(open);
			
	
	}
}
